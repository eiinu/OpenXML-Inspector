use std::{fs, path::PathBuf};

const FALLBACK_ICON_PNG: &[u8] = &[
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d, 0x49, 0x48,
    0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x06, 0x00, 0x00,
    0x00, 0x1f, 0x15, 0xc4, 0x89, 0x00, 0x00, 0x00, 0x0d, 0x49, 0x44, 0x41, 0x54, 0x78,
    0x9c, 0x63, 0xf8, 0xcf, 0xc0, 0xf0, 0x1f, 0x00, 0x05, 0x00, 0x01, 0xff, 0x89, 0x99,
    0x3d, 0x1d, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82,
];

fn main() {
    ensure_fallback_icon();
    tauri_build::build()
}

fn ensure_fallback_icon() {
    let manifest_dir = PathBuf::from(std::env::var("CARGO_MANIFEST_DIR").expect("missing CARGO_MANIFEST_DIR"));
    let icons_dir = manifest_dir.join("icons");
    let icon_path = icons_dir.join("icon.png");

    if icon_path.exists() {
        return;
    }

    fs::create_dir_all(&icons_dir).expect("failed to create src-tauri/icons");
    fs::write(&icon_path, FALLBACK_ICON_PNG).expect("failed to write fallback Tauri icon");
}
